#include "so_long.h"

void	ft_putnbr(int n)
{
	if (!n)
	{
		write(1, "0", 1);
		return ;
	}
	if (n > 9)
	{
		ft_putnbr(n / 10);
		n %= 10;
	}
	n += '0';
	write(1, &n, 1);
}
